-- Vim Settings
vim.cmd("set expandtab") -- Converts tabs into spaces
vim.cmd("set tabstop=2") -- Sets the number of spaces
vim.cmd("set softtabstop=2") -- Sets tab deletion to two spaces per tab
vim.cmd("set shiftwidth=2") -- Also sets the number of spaces per tab

--  Vim Mappings setup
vim.g.mapleader = " "
